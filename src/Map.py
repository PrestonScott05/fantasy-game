    def create_rect_grid(self):
        new_grid = []
        for y, row in enumerate(self.grid):
            for x, cell in enumerate(row):
                if cell == 0:
                    rect = py.Rect(
                        self.offset_x + x * self.tile_size,
                        self.offset_y + y * self.tile_size,
                        self.tile_size,
                        self.tile_size,
                    )
                    new_grid.append(rect)
        return new_grid

